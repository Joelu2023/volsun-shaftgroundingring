const heroCtaLabelKeys = {
  sample: "heroCtaSample",
  quote: "heroCtaQuote",
  drawing: "heroCtaDrawing",
  engineer: "heroCtaEngineer",
} as const;

export function heroCtaLabel(ctaKey: CtaKey, locale: AppLocale): string {
  const u = uiMessages[locale];
  const k = heroCtaLabelKeys[ctaKey as keyof typeof heroCtaLabelKeys];
  if (!k) return ctaKey;
  return (u as unknown as Record<string, string>)[k] ?? ctaKey;
}

export function ui(locale: AppLocale) {
  return uiMessages[locale];
}

export function navLabel(href: string, locale: AppLocale): string {
  const n = uiMessages[locale].nav as Record<string, string>;
  return n[href] ?? href;
}
